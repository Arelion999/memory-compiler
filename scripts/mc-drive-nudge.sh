#!/bin/bash
# Заставить Synology Drive заметить статьи, записанные КОНТЕЙНЕРОМ.
#
# ПРОБЛЕМА (установлена замером 2026-07-19). Synology Drive синхронизирует
# knowledge/ в обе стороны и делает это за секунды, но НЕ ВИДИТ записи, сделанные
# контейнером memory-compiler через bind-mount. Проверено зондами:
#   * файл, созданный на ПК          -> на NAS в ту же секунду;
#   * удалённый на ПК                -> исчез с NAS за 45 с;
#   * созданный на NAS в корне       -> на ПК за 20 с;
#   * созданный на NAS в подпапке    -> на ПК за 40 с;
#   * созданный через переименование из дотфайла (как пишет сам сервер) -> доехал;
#   * НИ ОДНА статья, записанная контейнером, -> не доехала.
# За три недели локальное зеркало отстало на 1239 статей из 1719 при полностью
# исправной, двусторонней синхронизации.
#
# ПОЧЕМУ ЭТО ОПАСНО, а не просто неудобно: синхронизация ЖИВАЯ. Удаление устаревших
# файлов в локальной папке уезжает на NAS и сносит статьи из боевой базы. Половинчатое
# зеркало выглядит заброшенным, и соблазн «прибраться» очень велик.
#
# ОБХОД. mtime в файловой системе КОРРЕКТЕН — его ставит сам контейнер; не видит
# изменений только механизм уведомлений Drive. touch снаружи контейнера порождает
# событие, которое Drive ловит, и файл уезжает вниз. Содержимое не меняется.
#
# УСТАНОВКА (нужен root, поэтому через DSM, а не из этого скрипта):
#   Панель управления -> Планировщик заданий -> Создать -> Запланированное задание
#   -> Пользовательский сценарий; пользователь root; расписание — каждые 5 минут;
#   команда:
#     bash /volume1/homes/Arelion/DEV/memory-compiler/scripts/mc-drive-nudge.sh
#
# Дотфайлы не трогаются: Drive их не носит вовсе (black_prefix = "." в его конфиге),
# а .whoosh_index и .embeddings.pkl переписываются постоянно — толкать их незачем.

K="${MC_KNOWLEDGE:-/volume1/homes/Arelion/DEV/memory-compiler/knowledge}"
STAMP="${MC_NUDGE_STAMP:-/var/log/mc-drive-nudge.stamp}"
LOG="${MC_NUDGE_LOG:-/var/log/mc-drive-nudge.log}"

[ -d "$K" ] || exit 0

# Первый запуск: считаем изменившимся только последний час, чтобы не толкать всю базу
# разом (это заставило бы Drive пере-хешировать 192 МБ на слабом CPU).
if [ ! -f "$STAMP" ]; then
    touch -d '1 hour ago' "$STAMP" 2>/dev/null || touch "$STAMP"
fi

n=$(find "$K" -type f -not -path '*/.*' -newer "$STAMP" -print -exec touch {} + 2>/dev/null | wc -l)

# Стамп ставится ПОСЛЕ обновления отметок: у тронутых файлов mtime уже не новее его,
# поэтому следующий прогон не подхватит их повторно и не зациклится. Известное узкое
# место: файл, записанный контейнером между find и этой строкой, будет пропущен до
# своей следующей правки — при пятиминутном расписании это редкий и самоисправляющийся
# случай.
touch "$STAMP"

if [ "${n:-0}" -gt 0 ]; then
    echo "[$(date '+%FT%T%z')] подтолкнул $n файл(ов)" >> "$LOG"
fi
exit 0
